#include <stdio.h>
#include <stdlib.h>

#include "goolib-error.h"
#include "goolib-jvm.h"

int main(int argc, char* argv[]) 
{
  char* error = NULL;
  int rc = goo_jvm_java(&error);
  error = goo_error_new("hello, world, %d", rc);
  printf("%s\n", error);
  if (rc != GOO_SUCCESS)
  {
    fprintf(stderr, "Error: %s\n", error);
    free(error);
    return GOO_ERROR_FAILURE;
  }
  return GOO_SUCCESS; 
}
